

import { NextRequest, NextResponse } from 'next/server';
import { createServerSupabaseClient } from '@/lib/auth-server';
import { prisma } from '@/lib/db';
import { z } from 'zod';

const createCategorySchema = z.object({
  name: z.string().min(1, 'Nome √© obrigat√≥rio'),
  icon: z.string().min(1, '√çcone √© obrigat√≥rio'),
  restaurantId: z.string().min(1, 'Restaurante √© obrigat√≥rio'),
});

export async function POST(request: NextRequest) {
  try {
    console.log('üîç [CATEGORIES API] Iniciando cria√ß√£o de categoria');
    
    const supabaseClient = createServerSupabaseClient();
    const { data: { session } } = await supabaseClient.auth.getSession();
    
    console.log('üîç [CATEGORIES API] Session user email:', session?.user?.email);
    
    if (!session?.user?.email) {
      console.log('‚ùå [CATEGORIES API] Usu√°rio n√£o autorizado - sem session');
      return NextResponse.json({ error: 'N√£o autorizado' }, { status: 401 });
    }

    const body = await request.json();
    console.log('üîç [CATEGORIES API] Body recebido:', JSON.stringify(body, null, 2));
    
    const { name, icon, restaurantId } = createCategorySchema.parse(body);
    console.log('üîç [CATEGORIES API] Dados parseados - name:', name, 'icon:', icon, 'restaurantId:', restaurantId);

    // Verificar se √© admin (bypass de verifica√ß√£o)
    const adminEmails = [
      "michaeldouglasqueiroz@gmail.com",
      "admin@onpedido.com"
    ];
    
    const isAdmin = adminEmails.includes(session.user.email);
    console.log('üîç [CATEGORIES API] √â admin?', isAdmin, 'Email:', session.user.email);
    
    if (!isAdmin) {
      console.log('üîç [CATEGORIES API] N√£o √© admin, verificando permiss√µes do usu√°rio...');
      // Verificar se o usu√°rio √© dono do restaurante (apenas para n√£o-admins)
      const user = await prisma.user.findUnique({
        where: { email: session.user.email },
        include: { restaurants: true }
      });
      
      console.log('üîç [CATEGORIES API] Usu√°rio encontrado:', user ? 'Sim' : 'N√£o');
      console.log('üîç [CATEGORIES API] Restaurantes do usu√°rio:', user?.restaurants?.map(r => r.id));

      if (!user || !user.restaurants || !user.restaurants.find((r: any) => r.id === restaurantId)) {
        console.log('‚ùå [CATEGORIES API] Usu√°rio n√£o autorizado para este restaurante');
        return NextResponse.json({ error: 'N√£o autorizado para este restaurante' }, { status: 403 });
      }
    } else {
      console.log('‚úÖ [CATEGORIES API] Admin detectado, bypass de verifica√ß√µes');
    }

    // Verificar se j√° existe uma categoria com o mesmo nome
    const existingCategory = await prisma.category.findFirst({
      where: {
        name,
        restaurantId
      }
    });

    if (existingCategory) {
      return NextResponse.json(
        { error: `J√° existe uma categoria com o nome "${name}" neste restaurante` },
        { status: 409 }
      );
    }

    console.log('üîç [CATEGORIES API] Tentando criar categoria no banco...');
    console.log('üîç [CATEGORIES API] Dados para cria√ß√£o:', { name, icon, restaurant_id: restaurantId });
    
    const { data, error } = await supabaseClient
      .from("categories")
      .insert([{
        name,
        icon,
        restaurant_id: restaurantId
      }])
      .select()
      .single();

    if (error) {
      console.error("Erro Supabase:", error); // üëà log no Vercel
      return NextResponse.json(
        { error: error.message, details: error },
        { status: 400 }
      );
    }
    
    console.log('‚úÖ [CATEGORIES API] Categoria criada com sucesso:', data.id);
    return NextResponse.json(data, { status: 201 });
  } catch (error) {
    console.error('Erro ao criar categoria:', error);
    
    if (error instanceof z.ZodError) {
      return NextResponse.json(
        { error: error.errors[0]?.message || 'Dados inv√°lidos' },
        { status: 400 }
      );
    }

    // Verificar se √© erro de constraint √∫nica do Prisma
    if (error instanceof Error && error.message.includes('Unique constraint')) {
      return NextResponse.json(
        { error: 'J√° existe uma categoria com este nome neste restaurante' },
        { status: 409 }
      );
    }

    return NextResponse.json(
      { error: 'Erro interno do servidor' },
      { status: 500 }
    );
  }
}

