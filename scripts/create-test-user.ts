import { createClient } from '@supabase/supabase-js';
import dotenv from 'dotenv';

// Carregar vari√°veis de ambiente
dotenv.config();

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL!;
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY!;

// Cliente com service role para criar usu√°rios
const supabase = createClient(supabaseUrl, supabaseServiceKey, {
  auth: {
    autoRefreshToken: false,
    persistSession: false
  }
});

async function createTestUser() {
  console.log('üîê Criando usu√°rio de teste no Supabase Auth...');
  
  try {
    // Criar usu√°rio no Supabase Auth
    const { data, error } = await supabase.auth.admin.createUser({
      email: 'teste@menudigital.com',
      password: '123456',
      email_confirm: true, // Confirmar email automaticamente
      user_metadata: {
        name: 'Usu√°rio Teste',
        full_name: 'Usu√°rio Teste'
      }
    });

    if (error) {
      console.error('‚ùå Erro ao criar usu√°rio:', error.message);
      return;
    }

    console.log('‚úÖ Usu√°rio de teste criado com sucesso!');
    console.log('üìß Email:', data.user.email);
    console.log('üÜî ID:', data.user.id);
    console.log('');
    console.log('üîë Credenciais de login:');
    console.log('   Email: teste@menudigital.com');
    console.log('   Senha: 123456');
    
  } catch (error) {
    console.error('‚ùå Erro inesperado:', error);
  }
}

// Executar o script
createTestUser()
  .then(() => {
    console.log('\nüéâ Script conclu√≠do!');
    process.exit(0);
  })
  .catch((error) => {
    console.error('‚ùå Erro no script:', error);
    process.exit(1);
  });